public class StaticDefVal {
    static int a;
    static float b;
    static double c;
    static long d;
    static short e;
    static byte f;
    static boolean g;
    public static void main(String[] args) {
        System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + g);
    }
}